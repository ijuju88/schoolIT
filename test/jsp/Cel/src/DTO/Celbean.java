package DTO;

public class Celbean {
	 
	 String title,url;
	 int sday, st, totday, lt;
	 
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public int getSday() {
		return sday;
	}
	public void setSday(int sday) {
		this.sday = sday;
	}
	public int getSt() {
		return st;
	}
	public void setSt(int st) {
		this.st = st;
	}
	public int getTotday() {
		return totday;
	}
	public void setTotday(int totday) {
		this.totday = totday;
	}
	public int getLt() {
		return lt;
	}
	public void setLt(int lt) {
		this.lt = lt;
	}
}
